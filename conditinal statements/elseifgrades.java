public class elseifgrades
{
	public static void main(String[] args) {
		int mark = 80;   

        if (mark >= 90) {
            System.out.println("A Grade");
        }
        else if (mark >= 75) {
            System.out.println("B Grade");
        }
        else if (mark >= 50) {
            System.out.println("C Grade");
        }
        else {
            System.out.println("Fail");
        }
	}
}
